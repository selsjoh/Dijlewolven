			<p>VOGELS HEBBEN VEREN</p>

			<p>Een vogel heeft veren. Niet slechts één soort, maar meerdere soorten voor verschillende functies; warmte, vliegvermogen, sturen en pronken.
			Vogels zijn er in vele soorten, maten, vormen en kleuren. Ze leggen eieren waaruit hun kuikens geboren worden; zoals deze meerkoeten- en futenkuikens. Een groot aantal vogels kan zwemmen, de meeste vogels kunnen ook vliegen.
			Maar waar alle vogels aan te herkennen zijn, is het feit dat hun lichaam bijna helemaal bedekt is met veren. Zo'n verenpakket bestaat niet uit één soort veren, maar uit verschillende soorten. De verschillende soorten veren hebben allemaal weer een andere functie.
			Veren zijn voor vogels van groot belang. Zo hebben vogels donsveertjes om warm te blijven. Daar overheen liggen dekveren, die de donsveertjes afdekken en beschermen. En vogels hebben slagpennen. Deze slagpennen hebben ze nodig om te kunnen vliegen en te sturen. Sommige vogels gebruiken ook nog sierveren om mee te kunnen pronken, zoals deze prachtige ogen in de staart van een pauw.</p>

