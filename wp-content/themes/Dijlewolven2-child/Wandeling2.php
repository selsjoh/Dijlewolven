			<p>De waterspeeltuin in het Sluispark is een symbool voor Leuven. De twee cirkels stellen de twee stadsmuren voor. De groeven staan voor de Dijlearmen , zoals ze door Leuven stromen. ’s Avonds verandert de waterspeeltuin in een prachtig verlichte fontein.</p>
			<p>Aan de oevers van de Dijle, bij de brug, staat een vijgenboom. Door de opwarming van het klimaat gedijt deze boom hier beter en beter. Hij heeft dikke leerachtige bladeren die goed tegen de zon kunnen. Zijn vruchten hebben (gedroogd of vers) laxerende eigenschappen.</p>
			<p>Let ook op de wilde hop. Humulus Lupulus heet de plant in het Latijn. Klinkt bijzonder vrolijk. Misschien omdat hop één van de belangrijkste ingrediënten is van bier. Vroeger zorgden de vrouwelijke hopbloemen (ook belletjes genoemd) ervoor dat bier beter bewaarde. Nu geeft hop het bier zijn typische smaak.</p>
