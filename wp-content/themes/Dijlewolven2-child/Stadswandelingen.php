		<div class="stadswandelingenTitel">WANDELEN DOOR STADSNATUUR</div>

		<div class="vhmTitel">‘Vrienden van Heverleebos en Meerdaalwoud vzw’</div>


		<div class="vhmTekst">is een regionale milieu- en natuurvereniging die, anders dan haar naam laat vermoeden, actief is in de regio rond en ten zuiden van Leuven. In tegenstelling tot de klassieke stadswandelingen brengt ze, in de levendige universiteitsstad aan de Dijle, ook stadsnatuur in het vizier.</div>



		<div class="stadswandelingenTekst">Zo ontdek je bijvoorbeeld dat in Leuven eeuwenoude bomen staan, verschillende soorten varens en dat je zelfs de zeldzame klimopbremraap kan ontdekken. Koestersoorten zoals de gierzwaluw,&nbsp; weidebeekjuffer en hazelworm kunnen je pad kruisen. Aan de hand van&nbsp; plannetjes, die ook als GPX-bestand kunnen gedownload worden (bruikbaar in de meeste GPS-programma’s), kan je deze wandelingen zelfstandig doen. <br>Wil je echter wat meer te weten komen, kan je steeds, voor een bescheiden prijs, een beroep doen op onze bekwame natuurgidsen.<br>
		Of kijk eens op onze website www.vhm.be, want op geregelde tijdstippen organiseren we gegidste wandelingen.</div>

