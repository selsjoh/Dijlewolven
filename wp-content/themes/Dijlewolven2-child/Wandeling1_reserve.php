		<div class="column2row3 specialeOpmaak">
			<p>De moeraseik komt uit Amerika. Zijn eikeltjes zijn pas rijp na twee jaar. Bovendien zijn ze kleiner en vallen met  minder dan de eikels van inlandse eiken.</p>
			<p>Deze boom staat hier sinds 1998. Het was een verjaardagscadeautje voor het 550-jarig bestaan van het stadhuis. Het dakgebinte van de stenen slagroomtaart van Leuven is immers  ook van eikenhout gemaakt.</p>
