		<div class="column2row2  specialeOpmaak"><h3>Opgepast! Overvliegende pijlen!</h3></div>
		<div class="column2row3 specialeOpmaak">
			<p>Het Sint-Donatuspark is bijna twee hectare groot. Het werd aangelegd omstreeks 1870 in Engelse landschapsstijl, met een vijver,  een kiosk en een statige wandeldreef met  de lichte schaduw van platanen. In de middeleeuwen was dit de droge gracht buiten de eerste ringmuur. Toen Leuven een tweede, grotere ringmuur bouwde mocht de kruisbooggilde hier oefenen. De ‘Gielenof’ noemen Leuvenaars hun park.</p>
			<p>In het Donatuspark vind je nog resten van de oude Leuvense ringmuur. De Donatustoren werd gerestaureerd in 2015. Bij de toren staan heel uitzonderlijk een mannelijke en een vrouwelijke Ginkgo Biloba. Extracten van deze bomen zijn bekend in de wetenschap. Ginkgo versterkt het geheugen en verbetert de concentratie.  In Japan staan de bomen symbool voor een lang leven. Als soort bestaat de Ginkgo Biloba namelijk al 200 miljoen jaar.</p>
