		<div class="column2row2 specialeOpmaak"><h3>Venijn bij de paus</h3></div>

		<div class="column2row3 specialeOpmaak">
			<p>Op de binnenplaats van het Pauscollege staan 2 venijnbomen  van bijna  200 jaar oud. Taxus wordt hij ook genoemd, maar die naam spreekt minder tot de verbeelding. Bovendien is de taxusboom erg giftig. Onderschat het venijn niet!</p>
			<p>Het Pauscollege is nog ouder dan de venijnboom. In 1523 schonk  Paus Adrianus VI zijn woonhuis bij testament aan de unief. Behoeftige theologiestudenten kregen hier kost en inwoon.</p>
			<p>Die arme studenten zijn verleden tijd. Nu wonen in de spleten van het Pauscollege gasten uit het zuiden: koppeltjes gierzwaluwen. Op zomeravonden hoor je ze roepen: wrrrrriiiiiih wrrrrriiiihhh. Ze scheren door de lucht op jacht naar insecten. Gierzwaluwen zijn supervliegers. Ze slapen zelfs in de lucht. Landen doen ze alleen om eitjes te leggen en uit te broeden. In het Pauscollege dus.</p>
