<div class="oplossingZoekspelTekst">
	<p>Speenkruid, Ficaria verna, behoort tot de ranonkelfamilie. Dit kleine, goudgele voorjaarsbloempje kreeg zijn naam omdat de vorm van de knolletjes lijkt op speentjes. Het werd vroeger gebruikt om aambeien (speen) te behandelen. Tijdens de bloei bevatten de bladeren giftige stoffen. Zelfs weidedieren mijden de plant dan. Bij slecht weer blijven de bloemen gesloten. De plant profiteert in de vroege lente van het zonlicht, dat door de kale bomen en struiken tot de bodem doordringt.</p>
</div>
